    /**
     * Create a list model from a list of hotels.
     * @param hotels - ArrayList of hotels.
     */
    public void createHotelsListModel(ArrayList<Hotel> hotels) {
        hotelsModel = new DefaultListModel<>();
        
        for (int i = 0; i < hotels.size(); i++) {
            hotelsModel.addElement("" + hotels.get(i).getID() + " " 
                    + hotels.get(i).getName() + " - Rating : " 
                    + hotels.get(i).getRating()); 
        }
        
        jHotelsList.setModel(hotelsModel);
    }
    
