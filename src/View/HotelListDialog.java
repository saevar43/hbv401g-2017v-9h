    /**
     * Action handler for "Available Rooms" button.
     * @param evt - button clicked.
     */
    private void jRoomsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRoomsButtonActionPerformed
        RoomControl rc = new RoomControl();
        HotelControl hc = new HotelControl();
        RoomsListDialog rld = new RoomsListDialog(mf, true);
        ArrayList<Room> rooms;
        
        int hotelId = hotelList.get(jHotelsList.getSelectedIndex()).getID();
        
        rooms = rc.getRoomsByHotel(hotelId);
        rld.setTitleText("Rooms available in " + hc.getHotelById(hotelId).getName() 
                + " - " + hc.getHotelById(hotelId).getLocation());
        rld.setHotelChosen(hc.getHotelById(hotelId));
        rld.setRoomsList(rooms);
        rld.createRoomsListModel(rooms);
        
        rld.setVisible(true);
    }//GEN-LAST:event_jRoomsButtonActionPerformed

